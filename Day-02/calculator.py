#using function approach to make a calculator

num1= 10
num2 = 40

def addition():
  sum= num1 + num2
  print("ADDITION IS :", sum)

def subtraction():
  sub= num2 - num1
  print("SUBTRACTION IS :", sub)

def multiplication():
  mul= num1 * num2
  print("MULTIPLICATION IS :", mul)

def division():
  div= num2 / num1
  print("DIVISION IS :", div)

addition()
subtraction()
multiplication()
division()
